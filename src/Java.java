public class Java {
	public static void main(String[] args){
	}
	private String message;

	public Java(String message){
		this.message = message;
	}
}
// private String message : 속성값을 나타냄
// public Java(String message){ this.message = message; } : 생성자를 나타냄
// 생성자 : 클래스 인스턴스를 생성할 때 실행되는 메서드로, 주로 속성을 초기화하는 용도로 사용
// static : 메서드에 static 키워드가 붙으면 클래스 메서드가 되어 객체를 만들지 않아도 클래스명.메서드명 형태로 호출이 가능하다

