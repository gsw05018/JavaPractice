public class Main {
	public static void main ( String[] args ) {
		System.out.println ( "Hello world!" );
	}
}
// public : 메서드의 접근 제어자로 누구나 이 메서드에 접근 가능
// static : 메서드에 static이 지정되어 있으면 이 메서드는 인스턴스를 생성하지 않아도 실행할 수 있다는 것을 의미
// void : 메서드의 리턴값이 없음을 의미
// String[] : 문자열을 나타내는 자바의 자료형 []은 배열을 의미
// args : 자료형의 변수명
// System.out.println : 표준 출력으로 데이터를 보내는 자바의 내장 메서드로 문자열을 화면에 출력